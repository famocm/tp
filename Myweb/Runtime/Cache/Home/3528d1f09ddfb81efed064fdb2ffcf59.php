        //获取属性值
        $(this).next().html("请输入6-18位数字字母及下划线的用户名").css('color','red');
        //添加类的样式
        $(this).css('border','1px solid #A9A9A9');
    })
    //检测昵称
    $("input[name='usernamee']").blur(function(){
        //获取昵称
        var username=$(this).val();
        // alert(username);
        if(username==''){
            // alert('sss');
            $(this).next().empty();
            $(this).css('border','1px solid #dddddd');
        }else{
           //正则
            var reg=/^\w{6,18}$/;
            if(!reg.test(username)){
                $(this).next().html('用户名格式错误');
                CNAME=false;
            }else{
                //Ajax
                s=$(this);
                $.get('/index.php/Home/Personal/checkename',{username:username},function(data){
                	// alert(data);
                    if(data==1){
                        s.next().html('用户名已存在').css('color','red');
                        CNAME=false;
                    }else{
                        s.css('border','1px solid #dddddd');
                        s.next().html("").css('color','green');
                        CNAME=true;
                    }
                })
            }  
        }
          
    })
